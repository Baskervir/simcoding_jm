package com.simcoding.datastructur.nonlinear.tree.temp;

public class MemberInfo {
    private final String name;
    private final int age;
    public MemberInfo(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
